    // MARK: - Remove Endpoints

    /// Remove a bridged endpoint.
    public func removeEndpoint(_ endpointID: EndpointID) {
        endpoints.removeEndpoint(endpointID)
        bridgedEndpoints.removeValue(forKey: endpointID)
    }

    /// Remove a bridged endpoint by handle.
    public func removeEndpoint(_ endpoint: BridgedEndpoint) {
        removeEndpoint(endpoint.endpointID)
    }

    // MARK: - Internal

    private func registerBridgedEndpoint(epID: EndpointID, name: String) -> BridgedEndpoint {
        let bridged = BridgedEndpoint(
            endpointID: epID,
            name: name,
            store: store,
            subscriptions: subscriptions
        )
        bridgedEndpoints[epID] = bridged

        // Per Matter spec §9.5.4.6, the Root Endpoint (ep=0) PartsList MUST list ALL endpoints.
        // The Aggregator Endpoint (ep=1) PartsList MUST list all its bridged endpoints.
        // Update both in the AttributeStore now that we know the new endpoint ID.
        let allBridgedIDs = bridgedEndpoints.keys.sorted { $0.rawValue < $1.rawValue }
        let bridgedElements = allBridgedIDs.map { TLVElement.unsignedInt(UInt64($0.rawValue)) }

        // ep=0 PartsList: aggregator (ep=1) + all bridged endpoints
        let rootParts = [TLVElement.unsignedInt(UInt64(EndpointManager.aggregatorEndpoint.rawValue))] + bridgedElements
        store.set(
            endpoint: EndpointID(rawValue: 0),
            cluster: ClusterID.descriptor,
            attribute: DescriptorCluster.Attribute.partsList,
            value: .array(rootParts)
        )

        // ep=1 PartsList: all bridged endpoints
        store.set(
            endpoint: EndpointManager.aggregatorEndpoint,
            cluster: ClusterID.descriptor,
            attribute: DescriptorCluster.Attribute.partsList,
            value: .array(bridgedElements)
        )

        return bridged
    }

    // MARK: - IM Handling

    /// Handle an incoming Interaction Model message.
    ///
    /// This is the primary entry point for IM processing. It delegates to the
    /// `InteractionModelHandler` which routes to `EndpointManager` and
    /// `SubscriptionManager` as appropriate.
    ///
    /// Returns an `IMHandleResult` — either immediate response pairs or a chunked
    /// report context when the response is too large for a single UDP message.
    public func handleIM(
        opcode: InteractionModelOpcode,
        payload: Data,
        sessionID: UInt16,
        fabricIndex: FabricIndex,
        exchangeID: UInt16 = 0,
        requestContext: IMRequestContext? = nil
    ) async throws -> IMHandleResult {
        try await imHandler.handleMessage(
            opcode: opcode,
            payload: payload,
            sessionID: sessionID,
            fabricIndex: fabricIndex,
            exchangeID: exchangeID,
            requestContext: requestContext
        )
    }

    // MARK: - Subscription Reports

    /// Get pending subscription reports.
    public func pendingReports(now: Date = Date()) async -> [PendingReport] {
        await subscriptions.pendingReports(now: now)
    }

    /// Build ReportData chunk(s) for a pending subscription report.
    ///
    /// Reads the current values of all subscribed attributes and any new events since
    /// the last report, then encodes them as one or more `ReportData` chunks using
    /// `ReportDataChunker`. Fabric-scoped attributes are filtered to the subscription's
    /// fabric when `isFabricFiltered` was `true` at subscribe time.
    ///
    /// Returns `nil` if the subscription no longer exists; otherwise returns one or more
    /// chunks (most reports fit in a single chunk).
    public func buildReport(for report: PendingReport) async -> [ReportData]? {
        async let pathsTask = subscriptions.attributePaths(for: report.subscriptionID)
        async let eventPathsTask = subscriptions.eventPaths(for: report.subscriptionID)
        async let isFabricFilteredTask = subscriptions.isFabricFiltered(for: report.subscriptionID)
        async let fabricIndexTask = subscriptions.fabricIndex(for: report.subscriptionID)
        async let lastEventNumberTask = subscriptions.lastEventNumber(for: report.subscriptionID)
        async let dvFiltersTask = subscriptions.dataVersionFilters(for: report.subscriptionID)

        guard let paths = await pathsTask else {
            return nil
        }

        let eventPaths = await eventPathsTask ?? []
        let isFabricFiltered = await isFabricFilteredTask
        let fabricIndex = await fabricIndexTask
        let lastEventNumber = await lastEventNumberTask ?? EventNumber(rawValue: 0)
        let dvFilters = await dvFiltersTask

        let attributeReports = endpoints.readAttributes(
            paths,
            fabricFiltered: isFabricFiltered,
            fabricIndex: fabricIndex,
            dataVersionFilters: dvFilters
        )

        // Query events since the last reported event number (only if event paths were subscribed)
        let eventReports: [EventReportIB]
        if !eventPaths.isEmpty {
            let eventMin = EventNumber(rawValue: lastEventNumber.rawValue + 1)
            eventReports = await endpoints.readEvents(eventPaths, eventMin: eventMin.rawValue > 1 ? eventMin : nil)
        } else {
            eventReports = []
        }

        // Update last event number if we got new events
        if let lastReport = eventReports.compactMap({ $0.eventData }).last {
            await subscriptions.updateLastEventNumber(
                for: report.subscriptionID,
                to: lastReport.eventNumber
            )
        }

        // Chunk the report (suppressResponse=false — subscriber must ack each chunk)
        let chunker = ReportDataChunker()
        let chunks = chunker.chunk(
            subscriptionID: report.subscriptionID,
            attributeReports: attributeReports,
            eventReports: eventReports,
            suppressResponseOnFinal: false
        )
        return chunks
    }

    /// Mark a report as sent (resets timers).
    public func reportSent(subscriptionID: SubscriptionID, now: Date = Date()) async {
        await subscriptions.reportSent(subscriptionID: subscriptionID, now: now)
    }

    /// Expire stale subscriptions.
    public func expireStale(now: Date = Date()) async -> [SubscriptionID] {
        await subscriptions.expireStale(now: now)
    }

    // MARK: - Accessors

    /// All currently registered bridged endpoints.
    public var allBridgedEndpoints: [BridgedEndpoint] {
        Array(bridgedEndpoints.values)
    }

    /// Get a bridged endpoint by ID.
    public func bridgedEndpoint(for id: EndpointID) -> BridgedEndpoint? {
        bridgedEndpoints[id]
    }
