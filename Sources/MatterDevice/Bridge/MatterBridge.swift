/// High-level API for a Matter bridge device.
///
/// `MatterBridge` creates a root endpoint (0) and aggregator endpoint (1) at init,
/// then allows dynamic addition/removal of bridged device endpoints. It wraps
/// `EndpointManager`, `AttributeStore`, `SubscriptionManager`, and
/// `InteractionModelHandler` into a single facade.
///
/// ```swift
/// let bridge = MatterBridge(config: .init(
///     vendorName: "Acumen", productName: "Hub",
///     vendorId: 0xFFF1, productId: 0x8000
/// ))
///
/// let light = bridge.addDimmableLight(name: "Kitchen Pendant")
/// await light.setOnOff(true)
/// await light.setLevel(200)
///
/// let responses = try await bridge.handleIM(
///     opcode: .readRequest,
///     payload: requestData,
///     sessionID: 1,
///     fabricIndex: FabricIndex(rawValue: 1)
/// )
/// ```
public final class MatterBridge: @unchecked Sendable {

    // MARK: - Config

    /// Bridge configuration.
    public struct Config: Sendable {
        public let vendorName: String
        public let productName: String
        public let vendorId: UInt16
        public let productId: UInt16

        public init(
            vendorName: String = "SwiftMatter",
            productName: String = "Bridge",
            vendorId: UInt16 = 0xFFF1,
            productId: UInt16 = 0x8000
        ) {
            self.vendorName = vendorName
            self.productName = productName
            self.vendorId = vendorId
            self.productId = productId
        }
    }

    // MARK: - State

    public let config: Config
    public let store: AttributeStore
    public let endpoints: EndpointManager
    public let subscriptions: SubscriptionManager
    public let commissioningState: CommissioningState
    public let timedRequestTracker: TimedRequestTracker
    /// Event store for recording cluster events generated by commands.
    public let eventStore: EventStore
    /// Storage for group key sets (GroupKeyManagement cluster).
    public let groupKeySetStorage = GroupKeySetStorage()
    /// Group membership table — tracks per-fabric endpoint group assignments.
    public let groupMembershipTable = GroupMembershipTable()
    /// Buffer for reassembling chunked write requests across multiple UDP messages.
    public let chunkedWriteBuffer = ChunkedWriteBuffer()
    /// Buffer for reassembling chunked invoke requests across multiple UDP messages.
    public let chunkedInvokeBuffer = ChunkedInvokeBuffer()
    private let imHandler: InteractionModelHandler
    private var bridgedEndpoints: [EndpointID: BridgedEndpoint] = [:]

    // MARK: - Init

    public init(
        config: Config = Config(),
        commissioningState: CommissioningState? = nil,
        fabricStore: (any MatterFabricStore)? = nil,
        attributeStore: (any MatterAttributeStore)? = nil
    ) {
        self.config = config
        self.commissioningState = commissioningState ?? CommissioningState(fabricStore: fabricStore)
        self.store = AttributeStore(attributeStore: attributeStore)
        self.eventStore = EventStore()
        self.endpoints = EndpointManager(store: store)
        self.endpoints.eventStore = self.eventStore
        self.subscriptions = SubscriptionManager()
        self.timedRequestTracker = TimedRequestTracker()
        self.imHandler = InteractionModelHandler(
            endpoints: endpoints,
            subscriptions: subscriptions,
            store: store,
            timedRequestTracker: timedRequestTracker,
            chunkedWriteBuffer: chunkedWriteBuffer,
            chunkedInvokeBuffer: chunkedInvokeBuffer
        )

        setupRootEndpoint()
        setupAggregatorEndpoint()
    }

    // MARK: - Root + Aggregator Setup

    private func setupRootEndpoint() {
        let rootClusters: [ClusterID] = [
            .descriptor,
            .basicInformation,
            .generalCommissioning,
            .networkCommissioning,
            .generalDiagnostics,
            .operationalCredentials,
            .accessControl,
            .adminCommissioning,
            .groupKeyManagement,
            .timeSynchronization,
        ]
        let root = EndpointConfig(
            endpointID: EndpointID(rawValue: 0),
            deviceTypes: [(.rootNode, 1)],
            clusterHandlers: [
                // Per Matter spec §9.5.4.6, the Root Endpoint's PartsList MUST list all other
                // endpoints on the node. We include the aggregator (ep=1) here; bridged endpoints
                // are added dynamically when registerBridgedEndpoint() updates the store directly.
                DescriptorHandler(
                    deviceTypes: [(.rootNode, 1)],
                    serverClusters: rootClusters,
                    partsList: [EndpointManager.aggregatorEndpoint]
                ),
                BasicInformationHandler(
                    vendorName: config.vendorName,
                    vendorID: config.vendorId,
                    productName: config.productName,
                    productID: config.productId
                ),
                GeneralCommissioningHandler(commissioningState: commissioningState),
                NetworkCommissioningHandler(),
                GeneralDiagnosticsHandler(),
                OperationalCredentialsHandler(commissioningState: commissioningState),
                AccessControlHandler(commissioningState: commissioningState),
                AdminCommissioningHandler(commissioningState: commissioningState),
                GroupKeyManagementHandler(
                    commissioningState: commissioningState,
                    keySetStorage: groupKeySetStorage
                ),
                TimeSynchronizationHandler(),
            ]
        )
        endpoints.addEndpoint(root)
    }

    private func setupAggregatorEndpoint() {
        let aggClusters: [ClusterID] = [.descriptor]
        let aggregator = EndpointConfig(
            endpointID: EndpointManager.aggregatorEndpoint,
            deviceTypes: [(.aggregator, 1)],
            clusterHandlers: [
                DescriptorHandler(
                    deviceTypes: [(.aggregator, 1)],
                    serverClusters: aggClusters
                )
            ]
        )
        endpoints.addEndpoint(aggregator)
    }

